        // month, day, year to day of year
        static void MDYToDoY(int month, int day, int year, out int doy)
        {
            doy = (Leap(year) ?
                DaysUptoMonthTotalLeap[month] : DaysUptoMonthTotal[month]) + day;
        }
        // day of year to month, day, year
        static void DoYToMDY(int doy, int year, out int month, out int day)
        {
            if (Leap(year))
            {
                month = MonthOfDoYLeap[doy];
                day = doy - DaysUptoMonthTotalLeap[month];
            }
            else
            {
                month = MonthOfDoY[doy];
                day = doy - DaysUptoMonthTotal[month];
            }
        }
        // is this a leap year?
        static bool Leap(int year)
        {
            return ((year % 4 == 0) && (year % 100 != 0 || year % 400 == 0));
        }
